/*==================================================================================FUNCTIONS=============================================================================*/

DROP FUNCTION IF EXISTS LayGhiChuLoi;
DELIMITER $$
CREATE FUNCTION LayGhiChuLoi(MaLoiCanTim VARCHAR(5)) RETURNS TEXT
DETERMINISTIC 
BEGIN
	RETURN (SELECT ET.GhiChu FROM ErrorTable ET WHERE ET.MaLoi = MaLoiCanTim  LIMIT 1);
END;
$$
DELIMITER ;

